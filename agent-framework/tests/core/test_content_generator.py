"""Tests for the smart content generator."""
from __future__ import annotations

from agent_framework.core.content_generator import generate_content


class TestPythonGeneration:
    def test_fastapi_file_generates_fastapi_app(self):
        content = generate_content("main.py", "fastapi app with CRUD routes")
        assert "FastAPI" in content
        assert "app.get" in content
        assert "app.post" in content

    def test_test_file_generates_pytest_suite(self):
        content = generate_content("tests/test_app.py", "pytest tests")
        assert "import pytest" in content
        assert "class Test" in content
        assert "def test_" in content

    def test_cli_file_generates_argparse(self):
        content = generate_content("cli.py", "command line CLI tool")
        assert "argparse" in content
        assert "def main" in content

    def test_data_file_generates_data_analysis(self):
        content = generate_content("analysis.py", "data analysis with pandas csv")
        assert "import csv" in content
        assert "def summarize" in content

    def test_generic_python_generates_dataclass(self):
        content = generate_content("models.py", "some python module")
        assert "dataclass" in content or "class " in content

    def test_test_prefix_forces_test_template(self):
        content = generate_content("test_models.py", "generic task")
        assert "import pytest" in content



class TestYearSubstitution:
    def test_readme_year_is_valid(self):
        content = generate_content("README.md", "")
        # Should not contain literal "{year}" or "{date[:4]}" — must be substituted
        assert "{year}" not in content
        assert "{date[" not in content
        import re
        years = re.findall(r"\b20\d{2}\b", content)
        assert len(years) >= 1, "Expected a 4-digit year in README"

    def test_no_unresolved_template_placeholders(self):
        import re
        # Template var names used in our ctx dict — these should all be substituted.
        template_vars = {"title", "name", "class_name", "date", "year"}
        for path in ["main.py", "config.json", "README.md", "Dockerfile", ".env.example"]:
            content = generate_content(path, "fastapi project")
            # Look for {word} patterns and flag only ones that are our template vars.
            found = re.findall(r"\{([a-z_]+)\}", content)
            leftover = [f for f in found if f in template_vars]
            assert leftover == [], f"{path} has unresolved template vars: {leftover}"

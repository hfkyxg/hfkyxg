_PY_DATA = '''\
"""Data analysis script: {title}

Generated by apathy on {date}.
"""
from __future__ import annotations

import csv
import json
import os
import statistics
from pathlib import Path


def load_csv(path: str) -> list[dict]:
    with open(path, newline="") as f:
        return list(csv.DictReader(f))


def summarize(records: list[dict]) -> dict:
    if not records:
        return {{"count": 0}}
    numeric: dict[str, list[float]] = {{}}
    for row in records:
        for k, v in row.items():
            try:
                numeric.setdefault(k, []).append(float(v))
            except (ValueError, TypeError):
                pass
    stats = {{"count": len(records)}}
    for col, values in numeric.items():
        stats[col] = {{
            "min": min(values),
            "max": max(values),
            "mean": round(statistics.mean(values), 4),
            "stdev": round(statistics.stdev(values), 4) if len(values) > 1 else 0,
        }}
    return stats


def main() -> None:
    data_dir = Path(os.environ.get("DATA_DIR", "."))
    csv_files = list(data_dir.glob("*.csv"))

    if not csv_files:
        print("No CSV files found. Creating sample dataset...")
        sample_path = data_dir / "sample.csv"
        with open(sample_path, "w", newline="") as f:
            w = csv.writer(f)
            w.writerow(["id", "value", "category"])
            for i in range(10):
                w.writerow([i + 1, (i + 1) * 3.14, "A" if i % 2 == 0 else "B"])
        csv_files = [sample_path]
        print(f"Created {{sample_path}}")

    for path in csv_files:
        records = load_csv(str(path))
        summary = summarize(records)
        print(f"\\n=== {{path.name}} ===")
        print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
'''


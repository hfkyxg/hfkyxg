# ── Public API ────────────────────────────────────────────────────────────────

def generate_content(path: str, task_text: str = "") -> str:
    """Return appropriate template content for *path* given *task_text* context."""
    ext = os.path.splitext(path)[1].lower()
    name = os.path.splitext(os.path.basename(path))[0]
    title = _title(path)
    class_name = re.sub(r"[^a-zA-Z0-9]", "", title.replace(" ", ""))
    if not class_name or not class_name[0].isalpha():
        class_name = "App" + class_name
    date = datetime.now().strftime("%Y-%m-%d")
    year = datetime.now().strftime("%Y")
    topic = _detect_topic(task_text + " " + path)

    ctx = {"title": title, "name": name, "class_name": class_name, "date": date, "year": year}

    if ext == ".py":
        if topic == "fastapi":
            return _PY_FASTAPI.format(**ctx)
        if topic == "cli":
            return _PY_CLI.format(**ctx)
        if topic == "test" or name.startswith("test_"):
            return _PY_TEST.format(**ctx)
        if topic == "data":
            return _PY_DATA.format(**ctx)
        return _PY_GENERAL.format(**ctx)

    if ext in (".sh", ".bash"):
        return _SH_GENERAL.format(**ctx)

    if ext in (".dockerfile",) or os.path.basename(path) == "Dockerfile":
        return _DOCKER_GENERAL.format(**ctx)

    if name == "requirements" and ext == ".txt":
        if topic == "fastapi":
            return _REQUIREMENTS_FASTAPI.format(**ctx)
        return _REQUIREMENTS_GENERAL.format(**ctx)

    if ext == ".json":
        return _JSON_CONFIG.format(**ctx)

    if ext in (".yaml", ".yml"):
        return _YAML_CONFIG.format(**ctx)

    if ext in (".html", ".htm"):
        return _HTML_GENERAL.format(**ctx)

    if ext == ".md":
        if name.lower() == "readme":
            return _MD_README.format(**ctx)
        return _MD_GENERAL.format(**ctx)

    if ext == ".txt":
        if topic == "todo" or "todo" in name.lower():
            return _TODO_TXT.format(**ctx)
        # plain text — use task as content if it looks like content
        return f"# {title}\nGenerated by apathy on {date}\n\n{task_text or 'Content goes here.'}\n"

    if name == ".env.example" or name == ".env":
        return _ENV_EXAMPLE.format(**ctx)

    if name == ".gitignore":
        return _GITIGNORE.format(**ctx)

    if name in ("Makefile", "makefile"):
        return _MAKEFILE.format(**ctx)

    # Fallback: generic marker so it's clear it came from apathy
    return f"# {title}\n# Generated by apathy on {date}\n# Task: {task_text or 'n/a'}\n"

"""Smart file content generator for the MockProvider.

Produces realistic, runnable template content based on file extension
and task context — no LLM needed. Used when MockProvider receives a
write_file request without explicit inline content.
"""
from __future__ import annotations

import os
import re
from datetime import datetime


def _title(path: str) -> str:
    """Derive a human-readable title from a file path."""
    name = os.path.splitext(os.path.basename(path))[0]
    return name.replace("_", " ").replace("-", " ").title()


def _detect_topic(task_text: str) -> str:
    """Return a broad topic tag from the task description."""
    lower = task_text.lower()
    if any(k in lower for k in ("fastapi", "api", "endpoint", "route", "rest")):
        return "fastapi"
    if any(k in lower for k in ("flask", "webapp", "web app")):
        return "flask"
    if any(k in lower for k in ("cli", "command line", "argparse", "typer", "click")):
        return "cli"
    if any(k in lower for k in ("test", "pytest", "unittest", "spec")):
        return "test"
    if any(k in lower for k in ("docker", "container", "image")):
        return "docker"
    if any(k in lower for k in ("data", "pandas", "csv", "analysis", "dataset")):
        return "data"
    if any(k in lower for k in ("todo", "task", "list", "checklist")):
        return "todo"
    if any(k in lower for k in ("config", "settings", "configuration")):
        return "config"
    if any(k in lower for k in ("readme", "documentation", "doc")):
        return "readme"
    return "general"


